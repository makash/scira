fn collect_candidate_files(target: &Path, matcher: &GlobSet, limitations: &mut Limitations) -> Result<Vec<PathBuf>> {
    let mut files = Vec::new();

    for entry in WalkDir::new(target)
        .follow_links(false)
        .into_iter()
        .filter_entry(|entry| !should_skip_scan_entry(entry))
    {
        match entry {
            Ok(entry) => {
                if !entry.file_type().is_file() {
                    continue;
                }
                let path = entry.path();
                let rel = path.strip_prefix(target).unwrap_or(path);
                let file_name = path.file_name().map(|n| n.to_string_lossy().to_string()).unwrap_or_default();
                let file_name_path = Path::new(&file_name);

                if !(matcher.is_match(rel) || matcher.is_match(file_name_path)) {
                    continue;
                }

                match entry.metadata() {
                    Ok(metadata) if metadata.len() > MAX_TEXT_FILE_BYTES => push_limited(
                        &mut limitations.not_inspected,
                        format!("{} (file larger than {} bytes)", path.display(), MAX_TEXT_FILE_BYTES),
                    ),
                    Ok(_) => files.push(path.to_path_buf()),
                    Err(err) => {
                        let is_permission_denied = err
                            .io_error()
                            .map(|e| e.kind() == io::ErrorKind::PermissionDenied)
                            .unwrap_or(false);
                        if is_permission_denied {
                            push_limited(&mut limitations.permission_denied_paths, path.display().to_string())
                        } else {
                            push_limited(
                                &mut limitations.not_inspected,
                                format!("{} ({err})", path.display()),
                            )
                        }
                    }
                }
            }
            Err(err) => {
                let path = err
                    .path()
                    .map(|p| p.display().to_string())
                    .unwrap_or_else(|| "unknown path".to_string());
                push_limited(&mut limitations.permission_denied_paths, path);
            }
        }
    }

    Ok(files)
}

fn scan_cache_hits(package: &str, roots: &[PathBuf], limitations: &mut Limitations) -> Result<Vec<CacheHit>> {
    let mut hits = Vec::new();
    let needle = package.to_lowercase();

    for root in dedupe_paths(roots) {
        if !root.exists() {
            continue;
        }
        for entry in WalkDir::new(&root)
            .follow_links(false)
            .into_iter()
            .filter_entry(|entry| !should_skip_scan_entry(entry))
        {
            match entry {
                Ok(entry) => {
                    let name = entry.file_name().to_string_lossy().to_lowercase();
                    if name.contains(&needle) {
                        hits.push(CacheHit {
                            source: classify_cache_source(&root),
                            path: entry.path().display().to_string(),
                            details: format!("Package-related cache artifact found under {}", root.display()),
                        });
                        if hits.len() >= 50 {
                            return Ok(hits);
                        }
                    }
                }
                Err(err) => {
                    let path = err
                        .path()
                        .map(|p| p.display().to_string())
                        .unwrap_or_else(|| root.display().to_string());
                    push_limited(&mut limitations.permission_denied_paths, path);
                }
            }
        }
    }

    Ok(hits)
}

fn search_ioc_files(
    roots: &[PathBuf],
    ioc_files: &[&str],
    limitations: &mut Limitations,
    skip_root: bool,
) -> Result<Vec<IocHit>> {
    let ioc_names: BTreeSet<String> = ioc_files.iter().map(|s| s.to_string()).collect();
    let mut hits = Vec::new();

    for root in dedupe_paths(roots) {
        if !root.exists() {
            continue;
        }
        for entry in WalkDir::new(&root)
            .follow_links(false)
            .into_iter()
            .filter_entry(|entry| !should_skip_scan_entry(entry))
        {
            match entry {
                Ok(entry) => {
                    if !entry.file_type().is_file() {
                        continue;
                    }
                    let file_name = entry.file_name().to_string_lossy().to_string();
                    if ioc_names.contains(&file_name) {
                        hits.push(IocHit {
                            indicator: file_name,
                            path: entry.path().display().to_string(),
                            line_number: None,
                            line: None,
                        });
                    }
                }
                Err(err) => {
                    let path = err
                        .path()
                        .map(|p| p.display().to_string())
                        .unwrap_or_else(|| root.display().to_string());
                    if !skip_root || path != root.display().to_string() {
                        push_limited(&mut limitations.permission_denied_paths, path);
                    }
                }
            }
        }
    }

    Ok(hits)
}

fn explain_report(profile: &IncidentProfile, report: &ScanReport, config: &LlmConfig) -> Result<ExplainOutput> {
    let system_prompt = build_explainer_system_prompt(profile);
    let user_prompt = build_explainer_user_prompt(profile, report)?;
    let explanation = match config.provider {
        LlmProvider::Anthropic => call_anthropic(config, &system_prompt, &user_prompt)?,
        LlmProvider::Openai => call_openai(config, &system_prompt, &user_prompt)?,
        LlmProvider::Gemini => call_gemini(config, &system_prompt, &user_prompt)?,
    };

    Ok(ExplainOutput {
        provider: config.provider.as_str().to_string(),
        model: config.model.clone(),
        explanation,
    })
}

fn call_anthropic(config: &LlmConfig, system_prompt: &str, user_prompt: &str) -> Result<String> {
    let client = Client::builder().timeout(std::time::Duration::from_secs(90)).build()?;
    let url = config
        .base_url
        .clone()
        .unwrap_or_else(|| "https://api.anthropic.com/v1/messages".to_string());

    let response: Value = client
        .post(url)
        .header("x-api-key", &config.api_key)
        .header("anthropic-version", "2023-06-01")
        .json(&json!({
            "model": config.model,
            "max_tokens": 1400,
            "system": system_prompt,
            "messages": [
                {"role": "user", "content": user_prompt}
            ]
        }))
        .send()?
        .error_for_status()?
        .json()?;

    let text = response
        .get("content")
        .and_then(|v| v.as_array())
        .and_then(|items| {
            items.iter().find_map(|item| {
                item.get("text")
                    .and_then(|text| text.as_str())
                    .map(|s| s.to_string())
            })
        })
        .ok_or_else(|| anyhow!("anthropic response did not contain text content"))?;

    Ok(text)
}

fn call_openai(config: &LlmConfig, system_prompt: &str, user_prompt: &str) -> Result<String> {
    let client = Client::builder().timeout(std::time::Duration::from_secs(90)).build()?;
    let url = config
        .base_url
        .clone()
        .unwrap_or_else(|| "https://api.openai.com/v1/chat/completions".to_string());

    let response: Value = client
        .post(url)
        .bearer_auth(&config.api_key)
        .json(&json!({
            "model": config.model,
            "temperature": 0.2,
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ]
        }))
        .send()?
        .error_for_status()?
        .json()?;

    let text = response
        .get("choices")
        .and_then(|v| v.as_array())
        .and_then(|choices| choices.first())
        .and_then(|choice| choice.get("message"))
        .and_then(|message| message.get("content"))
        .and_then(|content| content.as_str())
        .map(|s| s.to_string())
        .ok_or_else(|| anyhow!("openai response did not contain message content"))?;

    Ok(text)
}

fn call_gemini(config: &LlmConfig, system_prompt: &str, user_prompt: &str) -> Result<String> {
    let client = Client::builder().timeout(std::time::Duration::from_secs(90)).build()?;
    let base = config
        .base_url
        .clone()
        .unwrap_or_else(|| "https://generativelanguage.googleapis.com/v1beta".to_string());
    let mut url = Url::parse(&format!("{}/models/{}:generateContent", base.trim_end_matches('/'), config.model))?;
    url.query_pairs_mut().append_pair("key", &config.api_key);

    let response: Value = client
        .post(url)
        .json(&json!({
            "system_instruction": {"parts": [{"text": system_prompt}]},
            "contents": [{"parts": [{"text": user_prompt}]}],
            "generationConfig": {"temperature": 0.2}
        }))
        .send()?
        .error_for_status()?
        .json()?;

    let text = response
        .get("candidates")
        .and_then(|v| v.as_array())
        .and_then(|candidates| candidates.first())
        .and_then(|candidate| candidate.get("content"))
        .and_then(|content| content.get("parts"))
        .and_then(|parts| parts.as_array())
        .and_then(|parts| parts.first())
        .and_then(|part| part.get("text"))
        .and_then(|text| text.as_str())
        .map(|s| s.to_string())
        .ok_or_else(|| anyhow!("gemini response did not contain text content"))?;

    Ok(text)
}

