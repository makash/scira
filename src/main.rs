fn render_scan_report_text(report: &ScanReport, verbose: bool) -> String {
    let mut out = String::new();
    out.push_str(&format!("Status: {}\n\n", report.summary.status.as_str()));
    out.push_str("Why:\n");
    out.push_str(&format!("- {}\n", report.summary.reason));

    let evidence = key_evidence_lines(report, verbose);
    if !evidence.is_empty() {
        for line in evidence {
            out.push_str(&format!("- {}\n", line));
        }
    }

    out.push_str("\nImmediate next steps:\n");
    for (idx, step) in report.actions.immediate_next_steps.iter().enumerate() {
        out.push_str(&format!("{}. {}\n", idx + 1, step));
    }

    if !report.actions.credential_rotation_scope.is_empty() {
        out.push_str("\nCredential rotation scope:\n");
        for item in &report.actions.credential_rotation_scope {
            out.push_str(&format!("- {}\n", item));
        }
    }

    if !report.limitations.permission_denied_paths.is_empty()
        || !report.limitations.missing_tools.is_empty()
        || !report.limitations.not_inspected.is_empty()
    {
        out.push_str("\nLimitations:\n");
        if !report.limitations.permission_denied_paths.is_empty() {
            out.push_str("- Some locations could not be inspected with the current user. Results may be incomplete.\n");
            if verbose {
                for path in report.limitations.permission_denied_paths.iter().take(10) {
                    out.push_str(&format!("  - denied: {}\n", path));
                }
            }
        }
        if !report.limitations.missing_tools.is_empty() {
            out.push_str(&format!(
                "- Some environment checks were skipped because tools were unavailable: {}\n",
                report.limitations.missing_tools.join(", ")
            ));
        }
        if !report.limitations.not_inspected.is_empty() && verbose {
            for item in report.limitations.not_inspected.iter().take(10) {
                out.push_str(&format!("  - not inspected: {}\n", item));
            }
        }
        out.push_str("- Re-run with sudo if you want broader host coverage.\n");
    }

    out.push_str("\nScan details:\n");
    out.push_str(&format!("- incident: {}\n", report.incident.id));
    out.push_str(&format!("- target: {}\n", report.scan.target));
    out.push_str(&format!("- user: {}\n", report.scan.user));
    out.push_str(&format!("- host: {}\n", report.scan.hostname));

    out
}

