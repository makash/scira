fn emit_output(rendered: &str, output: Option<&PathBuf>) -> Result<()> {
    match output {
        Some(path) => {
            fs::write(path, rendered)
                .with_context(|| format!("failed writing output file {}", path.display()))?;
            if io::stdout().is_terminal() {
                eprintln!("saved output to {}", path.display());
            }
        }
        None => {
            println!("{}", rendered);
        }
    }
    Ok(())
}

fn ensure_target_is_accessible(target: &Path) -> Result<()> {
    if !target.exists() {
        return Err(anyhow!("target path does not exist: {}", target.display()));
    }

    if target.is_dir() {
        fs::read_dir(target)
            .with_context(|| {
                format!(
                    "target path exists but cannot be inspected with the current user: {}\n\nRe-run with sudo if you intend to inspect this location.",
                    target.display()
                )
            })?
            .next();
    } else {
        fs::File::open(target).with_context(|| {
            format!(
                "target path exists but cannot be inspected with the current user: {}\n\nRe-run with sudo if you intend to inspect this location.",
                target.display()
            )
        })?;
    }

    Ok(())
}

fn prompt_yes_no(prompt: &str, default_yes: bool) -> Result<bool> {
    print!("{}", prompt);
    io::stdout().flush()?;
    let mut input = String::new();
    io::stdin().read_line(&mut input)?;
    let trimmed = input.trim().to_lowercase();
    if trimmed.is_empty() {
        return Ok(default_yes);
    }
    Ok(matches!(trimmed.as_str(), "y" | "yes"))
}

