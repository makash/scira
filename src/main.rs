#[derive(Subcommand, Debug)]
enum Commands {
    /// Scan a target path or broad host-visible paths for a bundled incident profile.
    Scan(ScanArgs),
    /// Explain a saved JSON report using an LLM.
    Explain(ExplainArgs),
}

#[derive(Args, Debug)]
struct ScanArgs {
    /// Bundled incident identifier. Current built-ins: litellm, axios
    incident: String,

    /// Scan this target path. Defaults to the current directory.
    #[arg(long, value_name = "PATH", conflicts_with = "all_dirs")]
    target: Option<PathBuf>,

    /// Broad scan using current user privileges.
    #[arg(long)]
    all_dirs: bool,

    #[arg(long, default_value = "text")]
    format: OutputFormat,

    /// Write the rendered report to a file.
    #[arg(long, value_name = "PATH")]
    output: Option<PathBuf>,

    /// Run AI explanation immediately after the scan.
    #[arg(long)]
    explain: bool,

    /// Never prompt for or run AI explanation.
    #[arg(long)]
    no_explain: bool,

    /// Disable interactive prompts.
    #[arg(long)]
    non_interactive: bool,

    /// Show more finding details in text output.
    #[arg(long)]
    verbose: bool,
}

