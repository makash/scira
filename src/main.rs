fn real_main() -> Result<i32> {
    let cli = Cli::parse();

    match cli.command {
        Commands::Scan(args) => run_scan_command(args),
        Commands::Explain(args) => run_explain_command(args),
    }
}

fn run_scan_command(args: ScanArgs) -> Result<i32> {
    let profile = incident_profile(&args.incident)?;
    let interactive = !args.non_interactive && io::stdin().is_terminal() && io::stdout().is_terminal();
    let llm_config = detect_llm_config()?;

    let target = if args.all_dirs {
        PathBuf::from("/")
    } else {
        match args.target {
            Some(path) => expand_tilde(path),
            None => env::current_dir().context("failed to determine current directory")?,
        }
    };

    if !args.all_dirs {
        ensure_target_is_accessible(&target)?;
    }

    let report = perform_scan(&profile, &target, args.all_dirs, interactive, llm_config.is_some())?;
    let rendered = match args.format {
        OutputFormat::Json => serde_json::to_string_pretty(&report)?,
        OutputFormat::Text => render_scan_report_text(&report, args.verbose),
    };

    emit_output(&rendered, args.output.as_ref())?;

    if args.explain {
        let config = llm_config.ok_or_else(missing_llm_config_error)?;
        let output = explain_report(&profile, &report, &config)?;
        println!("\n{}", render_explain_text(&output));
    } else if !args.no_explain && args.format == OutputFormat::Text && interactive {
        if let Some(config) = llm_config {
            if prompt_yes_no("\nScan complete. Would you like an AI explanation and remediation summary? [Y/n] ", true)? {
                let output = explain_report(&profile, &report, &config)?;
                println!("\n{}", render_explain_text(&output));
            }
        } else {
            println!(
                "\nTip: AI explanation is not available because no LLM API key was found.\nSet SCIRA_LLM_API_KEY to enable explanation. scira will try to detect the provider and choose a default model automatically.\nIf auto-detection does not work, also set SCIRA_LLM_PROVIDER."
            );
        }
    }

    Ok(exit_code_for_status(report.summary.status))
}

fn run_explain_command(args: ExplainArgs) -> Result<i32> {
    let report_raw = fs::read_to_string(&args.report)
        .with_context(|| format!("failed reading report file {}", args.report.display()))?;
    let report: ScanReport = serde_json::from_str(&report_raw)
        .with_context(|| format!("failed parsing JSON report {}", args.report.display()))?;

    let incident_id = args.incident.unwrap_or_else(|| report.incident.id.clone());
    let profile = incident_profile(&incident_id)?;
    let config = detect_llm_config()?.ok_or_else(missing_llm_config_error)?;
    let output = explain_report(&profile, &report, &config)?;

    let rendered = match args.format {
        OutputFormat::Json => serde_json::to_string_pretty(&output)?,
        OutputFormat::Text => render_explain_text(&output),
    };
    emit_output(&rendered, args.output.as_ref())?;

    Ok(0)
}

fn perform_scan(
    profile: &IncidentProfile,
    target: &Path,
    all_dirs: bool,
    interactive: bool,
    llm_available: bool,
) -> Result<ScanReport> {
    let started_at = Utc::now();
    let timer = Instant::now();

    let mut limitations = Limitations::default();
    let mut findings = Findings {
        manifest_hits: Vec::new(),
        version_hits: Vec::new(),
        ioc_file_hits: Vec::new(),
        ioc_domain_hits: Vec::new(),
        environment_hits: Vec::new(),
        cache_hits: Vec::new(),
        skipped_checks: Vec::new(),
    };

    let matcher = build_matcher(profile.search_patterns)?;
    let candidate_files = collect_candidate_files(target, &matcher, &mut limitations)?;
    scan_candidate_files(profile, target, &candidate_files, &mut findings, &mut limitations)?;

    let environment_scan = scan_environment(profile, target, &mut limitations)?;
    findings.environment_hits.extend(environment_scan.findings);
    findings.cache_hits.extend(environment_scan.cache_hits);
    findings.ioc_file_hits.extend(environment_scan.ioc_file_hits);
    findings.skipped_checks.extend(environment_scan.skipped_checks);

    let summary = build_summary(&findings, &limitations);
    let actions = build_actions(profile, &summary, &limitations);
    let completed_at = Utc::now();

    Ok(ScanReport {
        schema_version: "1".to_string(),
        incident: IncidentMetadata {
            id: profile.id.to_string(),
            title: profile.title.to_string(),
            ecosystem: profile.ecosystem.to_string(),
            package: profile.package.to_string(),
            bad_versions: profile.bad_versions.iter().map(|s| s.to_string()).collect(),
            safe_version: profile.safe_version.to_string(),
            ioc_domains: profile.ioc_domains.iter().map(|s| s.to_string()).collect(),
            ioc_files: profile.ioc_files.iter().map(|s| s.to_string()).collect(),
            source: IncidentSource {
                repo: profile.source_repo.to_string(),
                skill: profile.source_skill.to_string(),
                version: profile.source_version.to_string(),
            },
        },
        scan: ScanMetadata {
            target: target.display().to_string(),
            mode: if all_dirs {
                "all_dirs".to_string()
            } else if target == env::current_dir().unwrap_or_else(|_| PathBuf::from(".")) {
                "default_target".to_string()
            } else {
                "explicit_target".to_string()
            },
            hostname: detect_hostname(),
            user: detect_user(),
            started_at: started_at.to_rfc3339(),
            completed_at: completed_at.to_rfc3339(),
            duration_ms: timer.elapsed().as_millis(),
            interactive,
            llm_available,
        },
        summary,
        findings,
        actions,
        limitations,
    })
}

fn scan_candidate_files(
    profile: &IncidentProfile,
    target_root: &Path,
    candidate_files: &[PathBuf],
    findings: &mut Findings,
    limitations: &mut Limitations,
) -> Result<()> {
    let package_res: Vec<Regex> = profile
        .related_package_names
        .iter()
        .map(|name| Regex::new(&format!(r"(?i)\b{}\b", regex::escape(name))))
        .collect::<Result<Vec<_>, _>>()?;
    let exact_pin_re = Regex::new(&format!(
        r"(?i)\b{}\b(?:\[[^\]]+\])?\s*==\s*([A-Za-z0-9._+-]+)",
        regex::escape(profile.package)
    ))?;

    for file in candidate_files {
        let content = match fs::read(file) {
            Ok(bytes) => String::from_utf8_lossy(&bytes).to_string(),
            Err(err) if err.kind() == io::ErrorKind::PermissionDenied => {
                push_limited(
                    &mut limitations.permission_denied_paths,
                    file.display().to_string(),
                );
                continue;
            }
            Err(err) => {
                push_limited(
                    &mut limitations.not_inspected,
                    format!("{} ({err})", file.display()),
                );
                continue;
            }
        };

        let file_name = file.file_name().and_then(|n| n.to_str()).unwrap_or_default();
        match file_name {
            "package.json" => parse_package_json(profile, file, &content, findings),
            "package-lock.json" => parse_package_lock_json(profile, file, &content, findings),
            _ => {}
        }

        for (idx, line) in content.lines().enumerate() {
            if package_res.iter().any(|re| re.is_match(line)) {
                let pinned_version = exact_pin_re
                    .captures(line)
                    .and_then(|caps| caps.get(1).map(|m| m.as_str().to_string()));

                findings.manifest_hits.push(ManifestHit {
                    path: file.display().to_string(),
                    line_number: idx + 1,
                    line: line.trim().to_string(),
                    pinned_version: pinned_version.clone(),
                    match_kind: if pinned_version.is_some() {
                        ManifestMatchKind::PinnedReference
                    } else {
                        ManifestMatchKind::LooseReference
                    },
                });

                if let Some(version) = pinned_version {
                    findings.version_hits.push(VersionHit {
                        path: file.display().to_string(),
                        line_number: idx + 1,
                        line: line.trim().to_string(),
                        status: classify_version(profile, &version),
                        version,
                    });
                }
            }

            for domain in profile.ioc_domains {
                if line.contains(domain) {
                    findings.ioc_domain_hits.push(IocHit {
                        indicator: domain.to_string(),
                        path: file.display().to_string(),
                        line_number: Some(idx + 1),
                        line: Some(line.trim().to_string()),
                    });
                }
            }
        }
    }

    findings.ioc_file_hits.extend(search_ioc_files(
        &[target_root.to_path_buf()],
        profile.ioc_files,
        limitations,
        false,
    )?);

    Ok(())
}

fn parse_package_json(profile: &IncidentProfile, file: &Path, content: &str, findings: &mut Findings) {
    let Ok(json) = serde_json::from_str::<Value>(content) else {
        return;
    };

    for section in ["dependencies", "devDependencies", "optionalDependencies", "peerDependencies"] {
        if let Some(deps) = json.get(section).and_then(|v| v.as_object()) {
            for name in profile.related_package_names {
                if let Some(version) = deps.get(*name).and_then(|v| v.as_str()) {
                    findings.manifest_hits.push(ManifestHit {
                        path: file.display().to_string(),
                        line_number: 0,
                        line: format!("{section}.{name} = {version}"),
                        pinned_version: if is_exact_npm_version(version) {
                            Some(version.to_string())
                        } else {
                            None
                        },
                        match_kind: if is_exact_npm_version(version) {
                            ManifestMatchKind::PinnedReference
                        } else {
                            ManifestMatchKind::LooseReference
                        },
                    });

                    if is_exact_npm_version(version) {
                        findings.version_hits.push(VersionHit {
                            path: file.display().to_string(),
                            line_number: 0,
                            line: format!("{section}.{name} = {version}"),
                            version: version.to_string(),
                            status: classify_version(profile, version),
                        });
                    }
                }
            }
        }
    }
}

fn parse_package_lock_json(profile: &IncidentProfile, file: &Path, content: &str, findings: &mut Findings) {
    let Ok(json) = serde_json::from_str::<Value>(content) else {
        return;
    };

    if let Some(packages) = json.get("packages").and_then(|v| v.as_object()) {
        for (package_path, package_meta) in packages {
            for name in profile.related_package_names {
                let node_module_suffix = format!("node_modules/{name}");
                if package_path == &node_module_suffix || package_path.ends_with(&format!("/{node_module_suffix}")) {
                    let version = package_meta
                        .get("version")
                        .and_then(|v| v.as_str())
                        .unwrap_or("unknown");
                    findings.manifest_hits.push(ManifestHit {
                        path: file.display().to_string(),
                        line_number: 0,
                        line: format!("packages[{package_path}] version {version}"),
                        pinned_version: Some(version.to_string()),
                        match_kind: ManifestMatchKind::PinnedReference,
                    });
                    findings.version_hits.push(VersionHit {
                        path: file.display().to_string(),
                        line_number: 0,
                        line: format!("packages[{package_path}] version {version}"),
                        version: version.to_string(),
                        status: classify_version(profile, version),
                    });
                }
            }
        }
    }

    if let Some(dependencies) = json.get("dependencies").and_then(|v| v.as_object()) {
        collect_package_lock_dependencies(profile, file, dependencies, Vec::new(), findings);
    }
}

fn collect_package_lock_dependencies(
    profile: &IncidentProfile,
    file: &Path,
    dependencies: &serde_json::Map<String, Value>,
    parent_chain: Vec<String>,
    findings: &mut Findings,
) {
    for (name, meta) in dependencies {
        let mut chain = parent_chain.clone();
        chain.push(name.clone());

        if profile.related_package_names.iter().any(|n| *n == name.as_str()) {
            let version = meta.get("version").and_then(|v| v.as_str()).unwrap_or("unknown");
            findings.manifest_hits.push(ManifestHit {
                path: file.display().to_string(),
                line_number: 0,
                line: format!("dependencies.{} = {}", chain.join(" > "), version),
                pinned_version: Some(version.to_string()),
                match_kind: ManifestMatchKind::PinnedReference,
            });
            findings.version_hits.push(VersionHit {
                path: file.display().to_string(),
                line_number: 0,
                line: format!("dependencies.{} = {}", chain.join(" > "), version),
                version: version.to_string(),
                status: classify_version(profile, version),
            });
        }

        if let Some(children) = meta.get("dependencies").and_then(|v| v.as_object()) {
            collect_package_lock_dependencies(profile, file, children, chain, findings);
        }
    }
}

fn scan_environment(profile: &IncidentProfile, target: &Path, limitations: &mut Limitations) -> Result<PythonEnvScan> {
    match profile.ecosystem {
        "python" => scan_python_environment(profile, limitations),
        "npm" => scan_npm_environment(profile, target, limitations),
        _ => Ok(PythonEnvScan::default()),
    }
}

fn scan_python_environment(profile: &IncidentProfile, limitations: &mut Limitations) -> Result<PythonEnvScan> {
    let mut result = PythonEnvScan::default();
    let python = detect_python();

    let Some(python) = python else {
        push_limited(&mut limitations.missing_tools, "python".to_string());
        result.skipped_checks.push(SkippedCheck {
            check: "python_environment".to_string(),
            reason: "python or python3 was not found".to_string(),
        });
        return Ok(result);
    };

    let pip_show = run_command_capture(&python, &["-m", "pip", "show", profile.package]);
    match pip_show {
        Ok(output) if output.status.success() && !output.stdout.trim().is_empty() => {
            let version = parse_pip_show_field(&output.stdout, "Version");
            let location = parse_pip_show_field(&output.stdout, "Location").unwrap_or_else(|| "unknown".to_string());
            result.findings.push(EnvironmentFinding {
                check: "pip_show".to_string(),
                status: "present".to_string(),
                details: format!("Installed package found via pip show at {location}"),
                bad_version: version
                    .as_ref()
                    .map(|v| profile.bad_versions.iter().any(|bad| *bad == v.as_str()))
                    .unwrap_or(false),
                version,
                raw: Some(output.stdout),
            });
        }
        Ok(output) if output.status.success() => {
            result.findings.push(EnvironmentFinding {
                check: "pip_show".to_string(),
                status: "not_present".to_string(),
                details: "Package not found via pip show".to_string(),
                version: None,
                bad_version: false,
                raw: None,
            });
        }
        Ok(output) => {
            result.findings.push(EnvironmentFinding {
                check: "pip_show".to_string(),
                status: "not_present".to_string(),
                details: "Package not found via pip show".to_string(),
                version: None,
                bad_version: false,
                raw: if output.stderr.trim().is_empty() { None } else { Some(output.stderr) },
            });
        }
        Err(err) => {
            if err.kind() == io::ErrorKind::NotFound {
                push_limited(&mut limitations.missing_tools, "pip".to_string());
            }
            result.skipped_checks.push(SkippedCheck {
                check: "pip_show".to_string(),
                reason: err.to_string(),
            });
        }
    }

    match run_command_capture(&python, &["-m", "pip", "freeze"]) {
        Ok(output) if output.status.success() => {
            let mut matched = false;
            for line in output.stdout.lines() {
                if line.to_lowercase().starts_with(&format!("{}==", profile.package.to_lowercase())) {
                    matched = true;
                    let version = line.split("==").nth(1).map(|s| s.trim().to_string());
                    result.findings.push(EnvironmentFinding {
                        check: "pip_freeze".to_string(),
                        status: "present".to_string(),
                        details: format!("Package appears in pip freeze: {}", line.trim()),
                        bad_version: version
                            .as_ref()
                            .map(|v| profile.bad_versions.iter().any(|bad| *bad == v.as_str()))
                            .unwrap_or(false),
                        version,
                        raw: Some(line.trim().to_string()),
                    });
                }
            }
            if !matched {
                result.findings.push(EnvironmentFinding {
                    check: "pip_freeze".to_string(),
                    status: "not_present".to_string(),
                    details: "Package not found in pip freeze".to_string(),
                    version: None,
                    bad_version: false,
                    raw: None,
                });
            }
        }
        Ok(output) => result.skipped_checks.push(SkippedCheck {
            check: "pip_freeze".to_string(),
            reason: stderr_or_fallback(&output.stderr, "pip freeze did not succeed"),
        }),
        Err(err) => result.skipped_checks.push(SkippedCheck {
            check: "pip_freeze".to_string(),
            reason: err.to_string(),
        }),
    }

    match run_command_capture(&python, &["-m", "pip", "inspect"]) {
        Ok(output) if output.status.success() => {
            let contains_package = output.stdout.to_lowercase().contains(&format!("\"name\": \"{}\"", profile.package.to_lowercase()));
            result.findings.push(EnvironmentFinding {
                check: "pip_inspect".to_string(),
                status: if contains_package { "present" } else { "completed" }.to_string(),
                details: if contains_package {
                    "Package appears in pip inspect output; review for dependency chain details".to_string()
                } else {
                    "pip inspect completed; package not clearly present in output".to_string()
                },
                version: None,
                bad_version: false,
                raw: None,
            });
        }
        Ok(output) => result.skipped_checks.push(SkippedCheck {
            check: "pip_inspect".to_string(),
            reason: stderr_or_fallback(&output.stderr, "pip inspect is not available"),
        }),
        Err(err) => result.skipped_checks.push(SkippedCheck {
            check: "pip_inspect".to_string(),
            reason: err.to_string(),
        }),
    }

    match run_command_capture("pipdeptree", &["-r", "-p", profile.package]) {
        Ok(output) if output.status.success() && !output.stdout.trim().is_empty() => {
            result.findings.push(EnvironmentFinding {
                check: "pipdeptree".to_string(),
                status: "present".to_string(),
                details: "Reverse dependency chain found via pipdeptree".to_string(),
                version: None,
                bad_version: false,
                raw: Some(output.stdout),
            });
        }
        Ok(_) => result.findings.push(EnvironmentFinding {
            check: "pipdeptree".to_string(),
            status: "not_present".to_string(),
            details: "No reverse dependency chain found via pipdeptree".to_string(),
            version: None,
            bad_version: false,
            raw: None,
        }),
        Err(err) => {
            if err.kind() == io::ErrorKind::NotFound {
                push_limited(&mut limitations.missing_tools, "pipdeptree".to_string());
            }
            result.skipped_checks.push(SkippedCheck {
                check: "pipdeptree".to_string(),
                reason: err.to_string(),
            });
        }
    }

    let mut env_paths = Vec::new();
    if let Some(home) = home_dir() {
        env_paths.push(home.join(".cache/pip"));
        env_paths.push(home.join(".cache/uv"));
    }

    if let Ok(output) = run_command_capture(
        &python,
        &["-c", "import json, site; paths=[]; user=site.getusersitepackages();\nif user: paths.append(user)\nfor p in getattr(site, 'getsitepackages', lambda: [])():\n    if p not in paths: paths.append(p)\nprint(json.dumps(paths))"],
    ) {
        if output.status.success() {
            if let Ok(paths) = serde_json::from_str::<Vec<String>>(&output.stdout) {
                for p in paths {
                    env_paths.push(PathBuf::from(p));
                }
            }
        }
    }

    result.cache_hits.extend(scan_cache_hits(profile.package, &env_paths, limitations)?);
    result.ioc_file_hits.extend(search_ioc_files(&env_paths, profile.ioc_files, limitations, true)?);

    Ok(result)
}

fn scan_npm_environment(profile: &IncidentProfile, target: &Path, limitations: &mut Limitations) -> Result<PythonEnvScan> {
    let mut result = PythonEnvScan::default();

    if !command_exists("npm") {
        push_limited(&mut limitations.missing_tools, "npm".to_string());
        result.skipped_checks.push(SkippedCheck {
            check: "npm_environment".to_string(),
            reason: "npm was not found".to_string(),
        });
        return Ok(result);
    }

    let target_has_node_context = target.join("package.json").exists()
        || target.join("package-lock.json").exists()
        || target.join("yarn.lock").exists()
        || target.join("pnpm-lock.yaml").exists()
        || target.join("node_modules").exists();

    if target_has_node_context {
        collect_npm_ls_findings(profile, target, false, &mut result);
        if let Some(dep) = profile.malicious_dependency {
            collect_npm_dependency_findings(profile, target, dep, "npm_ls_malicious_dep", false, &mut result);
        }
    } else {
        result.skipped_checks.push(SkippedCheck {
            check: "npm_ls".to_string(),
            reason: format!("target {} does not look like a Node/npm project", target.display()),
        });
    }

    collect_npm_ls_findings(profile, Path::new("/"), true, &mut result);
    if let Some(dep) = profile.malicious_dependency {
        collect_npm_dependency_findings(profile, Path::new("/"), dep, "npm_ls_global_malicious_dep", true, &mut result);
    }

    let mut env_paths = Vec::new();
    if let Some(home) = home_dir() {
        env_paths.push(home.join(".npm/_cacache/index-v5"));
        env_paths.push(home.join(".npm/_cacache"));
        env_paths.push(home.join(".npm"));
    }

    result.cache_hits.extend(scan_cache_hits(profile.package, &env_paths, limitations)?);
    if let Some(dep) = profile.malicious_dependency {
        result.cache_hits.extend(scan_cache_hits(dep, &env_paths, limitations)?);
    }
    result.ioc_file_hits.extend(search_ioc_files(&env_paths, profile.ioc_files, limitations, true)?);

    Ok(result)
}

fn build_summary(findings: &Findings, limitations: &Limitations) -> Summary {
    let likely_affected = findings
        .version_hits
        .iter()
        .any(|hit| matches!(hit.status, VersionHitStatus::Compromised))
        || findings.environment_hits.iter().any(|f| f.bad_version)
        || !findings.ioc_file_hits.is_empty()
        || !findings.ioc_domain_hits.is_empty();

    let references_found = !findings.manifest_hits.is_empty()
        || findings.environment_hits.iter().any(|f| f.status == "present")
        || !findings.cache_hits.is_empty();

    let status = if likely_affected {
        IncidentStatus::LikelyAffected
    } else if references_found {
        IncidentStatus::ReferencesFoundFollowUpNeeded
    } else {
        IncidentStatus::NoIndicatorsFound
    };

    let reason = match status {
        IncidentStatus::LikelyAffected => {
            if findings
                .version_hits
                .iter()
                .any(|hit| matches!(hit.status, VersionHitStatus::Compromised))
                || findings.environment_hits.iter().any(|f| f.bad_version)
            {
                "A compromised version or strong IOC evidence was found during the scan.".to_string()
            } else {
                "IOC evidence was found even though a compromised version was not confirmed in the scanned manifests.".to_string()
            }
        }
        IncidentStatus::ReferencesFoundFollowUpNeeded => {
            "The package or related references were found, but the scan did not confirm a compromised version.".to_string()
        }
        IncidentStatus::NoIndicatorsFound => {
            "No indicators were found in the checks that completed successfully.".to_string()
        }
    };

    let mut confidence_notes = Vec::new();
    if !limitations.permission_denied_paths.is_empty() {
        confidence_notes.push("Some paths could not be inspected with the current user. Results may be incomplete.".to_string());
    }
    if !limitations.missing_tools.is_empty() {
        confidence_notes.push(format!(
            "Some environment checks were skipped because tools were unavailable: {}.",
            limitations.missing_tools.join(", ")
        ));
    }
    if confidence_notes.is_empty() {
        confidence_notes.push("Deterministic findings shown here are the source of truth for this run.".to_string());
    }

    Summary {
        status,
        reason,
        confidence_notes,
    }
}

